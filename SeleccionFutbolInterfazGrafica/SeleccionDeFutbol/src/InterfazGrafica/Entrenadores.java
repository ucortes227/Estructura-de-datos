    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(jTextField1.getText());
        String nombre = jTextField2.getText();
        String apellido = jTextField3.getText();
        int edad = Integer.parseInt(jTextField4.getText());
        String idfederacion = jTextField5.getText();
        Entrenador entrenador = new Entrenador(id, nombre, apellido, edad, idfederacion);
        Menu.integrantes.add(entrenador);
        JOptionPane.showMessageDialog(this, "Entrenador guardado");

        mostrarRegistros();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void mostrarRegistros() {
        Vector<String> columnas = new Vector<>();
        columnas.add("ID");
        columnas.add("Nombre");
        columnas.add("Apellido");
        columnas.add("Edad");
        columnas.add("ID Federacion");

        Vector datos = new Vector();

        for (SeleccionFutbol masajista : Menu.integrantes) {
            if (masajista instanceof Entrenador) {
                Vector row = new Vector();
                row.add(masajista.getId());
                row.add(masajista.getNombre());
                row.add(masajista.getApellidos());
                row.add(masajista.getEdad());
                row.add(((Entrenador) masajista).getIdFederacion());

                datos.add(row);
            }

        }

        DefaultTableModel model = new DefaultTableModel(datos, columnas);
        jTable1.setModel(model);
    }
    
